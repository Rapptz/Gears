#ifndef GEARS_SYSTEM_HPP
#define GEARS_SYSTEM_HPP

#include "system/process.hpp"

/**
 * @defgroup system System Module
 * @brief Provides a portable abstraction layer to operating system operations.
 * @details This module is meant to provide an abstraction layer that helps with
 * the dealing of operating system specific functions such as process management.
 * The main goal of this module is to provide something similar to Python's
 * `subprocess` and `sys` modules. This module aims to make this abstraction layer
 * painless to use. The operating systems supported are POSIX compatible (Linux,
 * Mac OS X, Cygwin), and Windows (Windows XP and higher).
 */

#endif // GEARS_SYSTEM_HPP
